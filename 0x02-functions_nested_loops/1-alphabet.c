#include "main.h"

/**
* main - Print the word "alphabets"
*
* Return: Always 0 (Success)
*/
void print_alphabet(void);

int main(void)
{
	void print_alphabet(void);
	{
		int i;
		char a[] = "abcdefghijklmnopqrstuvwxyz";
		for (i = 0; i < 26; i++)
		{
		_putchar(a[i]);
		}
		_putchar('\n');
		return (0);
	}
	print_alphabet();
	return (0);
}
